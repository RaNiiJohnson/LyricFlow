// Helper functions
export const getAlbumById = (id: string): Album | undefined => {
  return mockAlbums.find((album) => album.id === id);
};

export const getAlbumsByArtist = (artistId: string): Album[] => {
  return mockAlbums.filter((album) => album.artistId === artistId);
};

export const getAllAlbums = (): Album[] => {
  return mockAlbums;
};

export const getAlbumsByGenre = (genre: string): Album[] => {
  return mockAlbums.filter(
    (album) => album.genre.toLowerCase() === genre.toLowerCase()
  );
};

// Async versions
export const getAllAlbumsAsync = async (): Promise<Album[]> => {
  return new Promise((resolve) => {
    setTimeout(() => resolve(mockAlbums), 0);
  });
};

export const getAlbumByIdAsync = async (
  id: string
): Promise<Album | undefined> => {
  return new Promise((resolve) => {
    setTimeout(() => resolve(mockAlbums.find((album) => album.id === id)), 0);
  });
};

export const getAlbumsByArtistAsync = async (
  artistId: string
): Promise<Album[]> => {
  return new Promise((resolve) => {
    setTimeout(
      () => resolve(mockAlbums.filter((album) => album.artistId === artistId)),
      0
    );
  });
};
